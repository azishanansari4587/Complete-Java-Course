package JavaBasics.Day3;

public class IfElseStatement {

    public static void main(String[] args) {

        //* TODO if-else statement: a block of code that is executed if a certain condition is true, and another block of code that is executed if the condition is false.

        int num = 10; //* 10 is an even number then condition will be true

        if ( num % 2 == 0 ) {
            System.out.println("The number is even.");
        } else {
            System.out.println("The number is odd.");
        }
    }
}
